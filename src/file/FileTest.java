package file;

import java.io.File;

public class FileTest {

	public static void main(String[] args) {
		//C드라이브에 하위에 존재하는 모든 디렉토리 및 파일을 출력해보자
		File file=new File("c:/");
		//하위 디렉토리 및 파일을 목록 추출
		File[] dir=file.listFiles(); //명단만받은것 //파일전체를 배열로 받기 String dir=file.list 보다 더 많이 받을 수 있음
		
		
		for(int i=0;i<dir.length;i++){
			//File file=dir[i];
			if(dir[i].isDirectory()){
				System.out.println(dir[i].getName());
			}
		}
	}
}
